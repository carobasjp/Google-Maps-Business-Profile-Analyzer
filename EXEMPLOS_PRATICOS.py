def enviar_alerta_email(assunto, mensagem):
    """Envia alerta por email quando detecta mudan√ßas significativas"""
    
    import smtplib
    from email.mime.text import MIMEText
    from email.mime.multipart import MIMEMultipart
    
    # Configura√ß√µes SMTP (Gmail)
    smtp_server = "smtp.gmail.com"
    smtp_port = 587
    remetente = "seu-email@gmail.com"
    senha = "sua-senha-app"  # Use senha de app, n√£o sua senha normal
    destinatario = "destinatario@gmail.com"
    
    # Cria mensagem
    msg = MIMEMultipart()
    msg['From'] = remetente
    msg['To'] = destinatario
    msg['Subject'] = assunto
    
    msg.attach(MIMEText(mensagem, 'plain'))
    
    # Envia
    try:
        server = smtplib.SMTP(smtp_server, smtp_port)
        server.starttls()
        server.login(remetente, senha)
        server.send_message(msg)
        server.quit()
        print("‚úÖ Email enviado com sucesso!")
    except Exception as e:
        print(f"‚ùå Erro ao enviar email: {e}")


